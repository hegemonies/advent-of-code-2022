private data class Rope(var x: Int, var y: Int, val isLast: Boolean = false) {
    fun moveTo(x: Int? = null, y: Int? = null) {
        this.x = x ?: this.x
        this.y = y ?: this.y
    }

    enum class Direction {
        N, NE, E, SE, S, SW, W, NW
    }

    private fun getDirection(other: Rope): Direction {
        if (x == other.x) {
            return if (other.y > y) {
                Direction.N
            } else {
                Direction.S
            }
        }

        if (y == other.y) {
            return if (other.x > x) {
                Direction.E
            } else {
                Direction.W
            }
        }

        return if (other.x > x) {
            if (other.y > y) {
                Direction.NE
            } else {
                Direction.SE
            }
        } else {
            if (other.y > y) {
                Direction.NW
            } else {
                Direction.SW
            }
        }
    }

    private fun getDirection2(other: Rope): Direction {
        return when {
            x == other.x && other.y > y -> Direction.N
            x == other.x && other.y < y -> Direction.S
            y == other.y && other.x > x -> Direction.E
            y == other.y && other.x < x -> Direction.W
            other.x > x && other.y > y -> Direction.NE
            other.x > x && other.y < y -> Direction.SE
            other.x < x && other.y > y -> Direction.NW
            other.x < x && other.y < y -> Direction.SW
            else -> throw IllegalStateException("Unexpected state")
        }
    }

    fun moveTo(other: Rope) {
        when (getDirection(other)) {
            Direction.N -> moveTo(y = y + 1)
            Direction.NE -> moveTo(x = x + 1, y = y + 1)
            Direction.E -> moveTo(x = x + 1)
            Direction.SE -> moveTo(x = x + 1, y = y - 1)
            Direction.S -> moveTo(y = y - 1)
            Direction.SW -> moveTo(x = x - 1, y = y - 1)
            Direction.W -> moveTo(x = x - 1)
            Direction.NW -> moveTo(x = x - 1, y = y + 1)
        }
    }

    fun isOtherInOurRadius(other: Rope): Boolean {
        return (x == other.x && y == other.y)
                || (x + 1 == other.x && y == other.y)
                || (x + 1 == other.x && y + 1 == other.y)
                || (x == other.x && y + 1 == other.y)
                || (x - 1 == other.x && y + 1 == other.y)
                || (x - 1 == other.x && y == other.y)
                || (x - 1 == other.x && y - 1 == other.y)
                || (x == other.x && y - 1 == other.y)
                || (x + 1 == other.x && y - 1 == other.y)
    }

    fun isDistanceMoreThenNumber(other: Rope, number: Int): Boolean {
        fun distance(x1: Int, y1: Int, x2: Int, y2: Int): Int {
            val base = abs(x1) - abs(x2)
            val altitude = abs(y1) - abs(y2)
            return round(sqrt(base * base + altitude * altitude + 0.0)).toInt()
        }

        return distance(x1 = x, y1 = y, x2 = other.x, y2 = other.y) > number
    }
}

private enum class StepDirection {
    U, D, L, R
}

private data class Step(val direction: StepDirection, val count: Int) {
    override fun toString(): String {
        return "$direction $count"
    }
}

private fun parseDay09Input(input: String): List<Step> {
    val stack = mutableListOf<Step>()
    input.lines()
        .map { line ->
            line.split(" ")
        }
        .map { (direction, count) ->
            Step(StepDirection.valueOf(direction), count.toInt())
        }
        .forEach { step ->
            stack.add(step)
        }
    return stack
}

private fun makeTraceKey(tail: Rope): String {
    return "${tail.x} ${tail.y}"
}

