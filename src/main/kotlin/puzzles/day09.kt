fun solveDay09Task(input: String): Int {
    val head = Rope(0, 0)
    val tail = Rope(0, 0)

    val tailTrace = mutableSetOf<String>()
    tailTrace.add(makeTraceKey(tail))

    val steps = parseDay09Input(input)

    steps.forEach { step ->
        repeat(step.count) {
            val prevHead = head.copy()

            when (step.direction) {
                StepDirection.U -> head.moveTo(y = head.y + 1)
                StepDirection.D -> head.moveTo(y = head.y - 1)
                StepDirection.L -> head.moveTo(x = head.x - 1)
                StepDirection.R -> head.moveTo(x = head.x + 1)
            }

            if (!tail.isOtherInOurRadius(head)) {
                tail.moveTo(prevHead)
                tailTrace.add(makeTraceKey(tail))
            }
        }
    }

    return tailTrace.size
}

